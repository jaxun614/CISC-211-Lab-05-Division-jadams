    /* r1: divisor/denominator */
    
    /* compair r0 and r1, check if r0 < r1 */
    /* cmp r0, r1 means (r0 - r1) */
    check:
    cmp r0, r1
    /* when r1(denominator) > r0(numorator), true */
    bgt greater
